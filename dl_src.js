// Clear the entire body
document.body.innerHTML = '';

// Iterate over each URL
obj.forEach(function(url, index) {
    // Delay the loading of each image, to not result in any failed requests
    setTimeout(function() {
        // Create a new img element
        var img = document.createElement('img');

        // Set the src attribute to the URL
        img.src = url;

        // Append the img element to the body
        document.body.appendChild(img);
    }, index * 400); // 400ms = 0.4s delay
});